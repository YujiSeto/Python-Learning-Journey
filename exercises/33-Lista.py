# crie uma lista com dois elementos numéricos.
lista=[8,9]

# adicione um novo elemento numérico à lista.
lista.append(10)

# mostre a nova lista.
print(lista)

# resultado.
# [8,9,10]

# adicione um string de caracteres à lista.
lista.append("palavra")
                
# mostre a nova lista.
print(lista)

# resultado.
# [8,9,10.’palavra’]